        <script>
            document.addEventListener("DOMContentLoaded", function () {
                const progress = document.getElementById("progress");
                const steps = document.querySelectorAll(".step");
                const timeline = document.getElementById("timeline");
                const lastStep = document.getElementById("last-step");

                // Safety check: stop if elements don't exist (e.g. on other pages)
                if (!progress || !steps.length || !timeline || !lastStep) return;

                const offset = parseInt(getComputedStyle(document.documentElement).getPropertyValue("--circle")) / 2;

                function updateProgress() {
                    const rect = timeline.getBoundingClientRect();
                    const timelineTop = window.scrollY + rect.top;
                    const centerY = window.scrollY + window.innerHeight / 2;

                    const lastStepRect = lastStep.getBoundingClientRect();
                    const lastStepCenter = window.scrollY + lastStepRect.top + lastStep.offsetHeight / 2;
                    const maxHeight = lastStepCenter - timelineTop - offset;

                    const scrollHeight = centerY - timelineTop - offset;
                    const clampedHeight = Math.max(0, Math.min(scrollHeight, maxHeight));
                    progress.style.height = `${clampedHeight}px`;

                    steps.forEach((step) => {
                        const stepRect = step.getBoundingClientRect();
                        const stepTop = window.scrollY + stepRect.top;
                        const stepCenter = stepTop + step.offsetHeight / 2;

                        if (centerY >= stepCenter - 50) {
                            step.classList.add("active");
                        } else {
                            step.classList.remove("active");
                        }
                    });
                }

                window.addEventListener("scroll", updateProgress);
                window.addEventListener("resize", updateProgress);
                window.addEventListener("load", updateProgress);
            });

            document.addEventListener("DOMContentLoaded", () => {
                const detailsElements = document.querySelectorAll(".footer-accordian details");

                const closeAccordion = (details) => {
                    const content = details.querySelector(".accordion-content");
                    if (!details.classList.contains("is-open")) return;

                    // Set current height to enable transition
                    content.style.maxHeight = content.scrollHeight + "px";

                    requestAnimationFrame(() => {
                        content.style.maxHeight = "0";
                        content.classList.remove("open");
                        details.classList.remove("is-open");
                    });

                    setTimeout(() => {
                        details.removeAttribute("open");
                    }, 400); // Match transition duration
                };

                const openAccordion = (details) => {
                    const content = details.querySelector(".accordion-content");
                    if (details.classList.contains("is-open")) return;

                    details.setAttribute("open", "");
                    details.classList.add("is-open");
                    content.classList.add("open");

                    // First set height so transition can animate from 0
                    content.style.maxHeight = content.scrollHeight + "px";

                    // After transition, remove maxHeight to allow natural growth
                    content.addEventListener("transitionend", function removeMaxHeight() {
                        if (details.classList.contains("is-open")) {
                            content.style.maxHeight = "none";
                        }
                        content.removeEventListener("transitionend", removeMaxHeight);
                    });
                };

                // Initial state
                detailsElements.forEach((details) => {
                    const content = details.querySelector(".accordion-content");
                    details.removeAttribute("open");
                    details.classList.remove("is-open");
                    if (content) {
                        content.classList.remove("open");
                        content.style.maxHeight = "0";
                    }
                });

                // Click handler
                detailsElements.forEach((details) => {
                    const summary = details.querySelector("summary");
                    summary.addEventListener("click", (event) => {
                        event.preventDefault();
                        if (details.classList.contains("is-open")) {
                            closeAccordion(details);
                        } else {
                            detailsElements.forEach((d) => {
                                if (d !== details) closeAccordion(d);
                            });
                            openAccordion(details);
                        }
                    });
                });
            });
