   // Function to save data to Supabase
   async function saveToSupabase(userData) {
       try {
           const { data, error } = await supabaseClient
               .from('Users')  // Using the Users table from Supabase
               .insert([userData]);
               
           if (error) {
               console.error('Error saving to Supabase:', error);
               alert("Demo booking failed. Please try again.");
           } else {
               console.log('Successfully saved to Supabase:', data);
               
               // Send email notification via EmailJS
               sendEmailNotification(userData);
               
               alert("Demo booked!");
               resetForm();
               modal.style.display = 'none';
           }
       } catch (err) {
           console.error('Exception when saving to Supabase:', err);
           alert("Demo booking failed. Please try again.");
       }
   }

   // Function to send email notification using EmailJS
