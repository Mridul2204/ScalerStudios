<script>
  // Hide loading when page is ready
  window.onload = function() {
    document.querySelector('.loading').style.display = 'none';
  };

  // jQuery ready
  $(document).ready(function() {
    // Show Bootstrap modal and load content when a button is clicked
    $('.trigger-btn').on('click', function() {
      const targetContent = $(this).data('target'); // Get content ID (content1 or content2)
      
      // Hide all content and only show the relevant one
      $('#content1, #content2').hide();
      $('#' + targetContent).show();

      const modal = new bootstrap.Modal(document.getElementById('videoGridModal'));
      modal.show();
    });

    // Magnific Popup for iframe video
    $('.mfp-gallery').magnificPopup({
      type: 'iframe',
      iframe: {
        patterns: {
          youtube: {
            index: 'youtube.com/',
            id: 'v=',
            src: 'https://www.youtube.com/embed/%id%?autoplay=1'
          }
        }
      },
      gallery: { enabled: true },
      callbacks: {
        open: function() {
          $('#videoGridModal').modal('hide');
        },
        close: function() {
          $('#videoGridModal').modal('show');
        }
      }
    });
  });
</script>



